function F=repeatedMeasures(Data)
%TrialType=[0; 1]
%Data=[2.20799947900000,1.12364494200000,1.85541228800000,1.76820302200000,1.48891840400000,2.26386894400000,0.818447518000000;1.00148827400000,0.343370108000000,0.663069478000000,1.44576224600000,0,0,0.390526536000000]
n=size(Data,2);
Mc=mean(Data,2);
Mm=mean(Data,1);
M=mean(mean(Data));
k=2;
SSb= (n * (Mc(1)-M)^2 ) + (n * (Mc(2)-M)^2 );
SSw= sum((Data(1,:)-Mc(1)).^2)+ sum((Data(2,:)-Mc(2)).^2);
SSs= k *sum((Mm-M).^2);

F = (n-1) * (SSb/(SSw-SSs));
end





